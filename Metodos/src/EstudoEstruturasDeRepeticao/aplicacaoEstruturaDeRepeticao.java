package EstudoEstruturasDeRepeticao;

import java.util.ArrayList;

public class aplicacaoEstruturaDeRepeticao {


    public static void main(String[] args) {
        //for como estrutura de repticao
        String alunos [] = {"Joao", "Marcos", "Felipe"};
        for(int i = 0; i < alunos.length; i++) System.out.println(alunos[i]);

        // for each para arrays
        for(String a : alunos) System.out.println(a);



    }


}
