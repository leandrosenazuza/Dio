package EstudoDeMetodosAplicacao.Funções;

public class Operacoes {

    public Long soma(long valor1, long valor2){
        return valor1 + valor2;
    }

    public Long subtracao(long valor1, long valor2){
        return valor1 - valor2;
    }

    public Long multiplicacao(long valor1, long valor2){
        return valor1 * valor2;
    }

    public Long divisao(long valor1, long valor2){
        return valor1 / valor2;
    }
}
